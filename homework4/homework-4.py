# Инициализация пустого списка
numbers = []

# Бесконечный цикл для ввода чисел
while True:
    user_input = input("Введите целое число или 'end' для завершения: ")

    # Если пользователь ввёл 'end', завершаем цикл
    if user_input.lower() == 'end':
        break
    
    try:
        # Преобразуем введённую строку в целое число
        number = int(user_input)
        
        # Добавляем число в список
        numbers.append(number)
    except ValueError:
        # Если не удаётся преобразовать в целое число, выводим ошибку
        print("Ошибка. Пожалуйста, введите целое число.")

# Подсчитываем количество чётных и нечётных чисел
a = 0
b = 0

for num in numbers:
    if num % 2 == 0:
        a += 1
    else:
        b += 1

# Выводим результаты
print(f"Четных чисел: {a}")
print(f"Нечетных чисел: {b}")